import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { spawnSync } from "node:child_process";

const ROOT = resolve(new URL("..", import.meta.url).pathname);
const manifestPath = resolve(ROOT, "wasm/worker-command-wasm/Cargo.toml");
const targetPath = resolve(
  ROOT,
  "wasm/worker-command-wasm/target/wasm32-unknown-unknown/release/worker_command_wasm.wasm",
);
const outputPath = resolve(ROOT, "src/lib/generated/worker-command-wasm.generated.js");
const bundledWasmPath = resolve(ROOT, "src/lib/generated/worker-command.wasm");

function runCargoBuild() {
  const result = spawnSync("cargo", [
    "build",
    "--release",
    "--target",
    "wasm32-unknown-unknown",
    "--manifest-path",
    manifestPath,
  ], {
    cwd: ROOT,
    stdio: "inherit",
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

function formatGeneratedModule(base64) {
  return `// Generated by scripts/build-worker-wasm.mjs. Do not edit by hand.
export const WORKER_COMMAND_WASM_BASE64 = ${JSON.stringify(base64)};
`;
}

async function main() {
  runCargoBuild();
  const wasm = await readFile(targetPath);
  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, formatGeneratedModule(wasm.toString("base64")));
  await writeFile(bundledWasmPath, wasm);
  process.stdout.write(`wrote ${outputPath}\n`);
  process.stdout.write(`wrote ${bundledWasmPath}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
