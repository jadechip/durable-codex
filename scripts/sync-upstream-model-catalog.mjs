import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const upstreamPath =
  process.env.UPSTREAM_CODEX_MODELS_JSON ||
  "/Users/jadechip/Code/neo-codex/codex/codex-rs/core/models.json";
const outputPath = path.resolve(__dirname, "../src/lib/model-catalog.generated.js");

function pickModelFields(model) {
  return {
    baseInstructions: typeof model.base_instructions === "string" ? model.base_instructions : null,
    modelMessages: model.model_messages ?? null,
  };
}

async function main() {
  const source = JSON.parse(await fs.readFile(upstreamPath, "utf8"));
  const models = Array.isArray(source?.models) ? source.models : [];
  const catalog = Object.fromEntries(
    models
      .filter((model) => typeof model?.slug === "string" && model.slug)
      .map((model) => [model.slug, pickModelFields(model)]),
  );

  const generated = `// Generated by scripts/sync-upstream-model-catalog.mjs\n// Source: ${upstreamPath}\n\nexport const MODEL_CATALOG = Object.freeze(${JSON.stringify(catalog, null, 2)});\n`;

  await fs.writeFile(outputPath, generated, "utf8");
  process.stdout.write(`Wrote ${Object.keys(catalog).length} model entries to ${outputPath}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
